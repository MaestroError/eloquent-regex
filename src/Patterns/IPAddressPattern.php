<?php
namespace Maestroerror\EloquentRegex\Patterns;

use Maestroerror\EloquentRegex\Patterns\BasePattern;
use Maestroerror\EloquentRegex\Traits\Pattern;

class IPAddressPattern extends BasePattern {

    use Pattern;

    // Regex pattern for IPv4
    protected string $pattern = "\b(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\b";

    public static string $name = "ipAddress";

    public static array $args = [];
}
